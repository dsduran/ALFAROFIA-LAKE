"historia",
"Inmerso en una finca de 41 hectareas, fue construido con la finalidad de abastecimiento de agua (regadío).
Su construcción terminó en Abril de 1992 (Fotos) y en Julio de ese mismo año, fue poblado con 1500 alevines
de carpa royal y 1000 alevines de black- blass. Desde su construcción, han tenido que transcurrir 15 años,
para encontrar los primeros pescadores de carpa. El black-bass siempre ha sido el pez más deseado por los 
lugareños y la especie que ha ocupado su tiempo libre."
