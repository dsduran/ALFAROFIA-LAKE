    - Debes tener en cuenta también que la resistencia mínima del hilo debe ser de 8 Kg (15lb).<br/><br/>
    - Las tiendas de campaña, paraguas, etc., deberán ser de color verde o camuflado.<br/><br/>
    - La entrada al agua para el lance o para cobrar una captura está permitida hasta la altura de las rodillas.<br/><br/>
    - Todo pescador deberá respetar el entorno, la flora y la fauna durante su estancia. La basura generada
