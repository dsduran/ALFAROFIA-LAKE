    "- Se podrá acceder a los puestos con el coche para descargar el material, pero luego deberán ser aparcados en
     un sitio habilitado.<br/><br/>
     - En este lago esta permitido el uso de barcos cebadores así como el de embarcaciones, ambas disponibles en
      las instalaciones.<br/><br/>
    - Además se debe respetar un máximo de dos pescadores por puesto con tres cañas cada uno. Los anzuelos deben 
    ser con micromuerte o sin muerte.<br/><br/>
    - La práctica de la captura y suelta es obligatoria, quedando prohibido el uso de sacos de retención; 
